def add(x, y):
    """
    Adds two numbers.

    Args:
        x (int): The first number.
        y (int): The second number.

    Returns:
        int: The sum of x and y.
    """
    return x + y

def subtract(x, y):
    """
    Subtracts two numbers.

    Args:
        x (int): The first number.
        y (int): The second number.

    Returns:
        int: The result of subtracting y from x.
    """
    return x - y
