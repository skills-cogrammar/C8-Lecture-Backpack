# Data Types and examples

# 1. **Integer (int)**:
a = 1
b = -1
print(a, b)  # Output: 1 -1

# 2. **Float (float)**:
pi = 3.14
negative_float = -1.0
print(pi, negative_float)  # Output: 3.14 -1.0

# 3. **Boolean (bool)**:
is_active = True
is_inactive = False
print(is_active, is_inactive)  # Output: True False

# 4. **String (str)**:
greeting = "Hello"
print(greeting)  # Output: Hello

# 5. **List (list)**:
numbers = [1, 2]
print(numbers)  # Output: [1, 2]

# 6. **Set (set)**:
mixed_set = {1, 'a'}
print(mixed_set)  # Output: {1, 'a'}

# 7. **Tuple (tuple)**:
coordinates = (1.0, 0)
print(coordinates)  # Output: (1.0, 0)

# 8. **Dictionary (dict)**:
point = {'x': 0.1, 'y': 0.2}
print(point)  # Output: {'x': 0.1, 'y': 0.2}

# Each of these examples shows how to create and print out the corresponding data type in Python.
